import UIKit

import Foundation
import UIKit

extension UIColor {
    // color
    static func MQYellowColor() -> UIColor {
        return UIColor(red: 253 / 255.0, green: 230 / 255.0, blue: 60 / 255.0, alpha:1)
    }
    static func MQRedColor() -> UIColor {
        return UIColor(red: 251 / 255.0, green: 109 / 255.0, blue: 116 / 255.0, alpha:1)
    }
   
    static func MQWhiteColor() -> UIColor {
        return UIColor(red: 255 / 255.0, green: 255 / 255.0, blue: 255 / 255.0, alpha:1)
    }
    
    
}

